import { savedSearchRouter } from "@/server/api/routers/savedSearch"; // Import the new router
import { userRouter } from "@/server/api/routers/user"; // Import the user router
import { createCallerFactory, createTRPCRouter } from "@/server/api/trpc";

/**
 * This is the primary router for your server.
 *
 * All routers added in /api/routers should be manually added here.
 */
export const appRouter = createTRPCRouter({
  savedSearch: savedSearchRouter, // Add the new router
  user: userRouter, // Add the user router
});

// export type definition of API
export type AppRouter = typeof appRouter;

/**
 * Create a server-side caller for the tRPC API.
 * @example
 * const trpc = createCaller(createContext);
 * const res = await trpc.post.all();
 *       ^? Post[]
 */
export const createCaller = createCallerFactory(appRouter);
